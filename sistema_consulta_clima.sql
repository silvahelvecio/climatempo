	select @dias = DATEDIFF(day, min(DataPrevisao), GETDATE())
	from PrevisaoClima

	update PrevisaoClima set DataPrevisao = DATEADD(day, @dias, DataPrevisao)
end
go

select * from PrevisaoClima order by DataPrevisao
